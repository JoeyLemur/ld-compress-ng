void require(bool condition, const char* message)
{
    if (!condition) {
        throw std::runtime_error(message);
    }
}

void write_bytes(const std::filesystem::path& path, const std::array<std::uint8_t, 395>& data)
{
    std::ofstream output(path, std::ios::binary);
    if (!output) {
        throw std::runtime_error("could not write " + path.string());
    }
    output.write(reinterpret_cast<const char*>(data.data()), static_cast<std::streamsize>(data.size()));
}

void test_ffmpeg_ogg_decode()
{
    const auto temp_dir = std::filesystem::temp_directory_path() /
        ("ld-compress-ng-reference-test-" + std::to_string(::getpid()));
    std::filesystem::remove_all(temp_dir);
    std::filesystem::create_directory(temp_dir);

    const auto ldf_path = temp_dir / "ffmpeg-reference.ldf";
    write_bytes(ldf_path, kFfmpegOggFlac);

    require(ldcompress::detect_flac_container(ldf_path.string()) == ldcompress::FlacContainer::Ogg,
        "reference fixture was not detected as Ogg FLAC");

    std::ostringstream decoded;
    const auto stats = ldcompress::decompress_flac_to_lds(ldf_path.string(), decoded);
    const auto decoded_data = decoded.str();
    const std::string expected(reinterpret_cast<const char*>(kReferenceLds.data()), kReferenceLds.size());

    require(stats.input_bytes == kFfmpegOggFlac.size(), "unexpected input byte count");
    require(stats.output_bytes == kReferenceLds.size(), "unexpected output byte count");
    require(stats.samples == 256, "unexpected decoded sample count");
    require(decoded_data == expected, "decoded reference Ogg FLAC changed LDS bytes");

    std::filesystem::remove_all(temp_dir);
}

